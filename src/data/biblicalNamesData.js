export const NAME_CATEGORIES = [
  "Todos",
  "Antigo Testamento",
  "Novo Testamento"
];

export function searchBiblicalNames(query, category = "Todos") {
  let results = BIBLICAL_NAMES_DATA;
  
  if (category !== "Todos") {
    results = results.filter(item => item.testament === category);
  }
  
  if (query && query.trim().length > 0) {
    const q = query.toLowerCase().trim();
    results = results.filter(item => 
      item.name.toLowerCase().includes(q) ||
      item.hebrew.includes(q) ||
      item.pronunciation.toLowerCase().includes(q) ||
      item.meaning.toLowerCase().includes(q) ||
      item.original.toLowerCase().includes(q) ||
      item.context.toLowerCase().includes(q) ||
      item.verses.some(v => v.toLowerCase().includes(q)) ||
      item.related.some(rel => rel.toLowerCase().includes(q))
    );
  }
  
  return results;
}
