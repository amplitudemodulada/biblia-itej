export function searchGlossary(query, category = "Todos") {
  let results = GLOSSARY_DATA;
  
  if (category !== "Todos") {
    results = results.filter(item => item.category === category);
  }
  
  if (query && query.trim().length > 0) {
    const q = query.toLowerCase().trim();
    results = results.filter(item => 
      item.term.toLowerCase().includes(q) ||
      item.hebrew.includes(q) ||
      item.pronunciation.toLowerCase().includes(q) ||
      item.meaning.toLowerCase().includes(q) ||
      item.examples.some(ex => ex.toLowerCase().includes(q)) ||
      item.related.some(rel => rel.toLowerCase().includes(q))
    );
  }
  
  return results;
}
